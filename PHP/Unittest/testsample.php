<?php


function som($var1, $var2) {
    // Bereken som van var1 en var2
    $uitkomst = $var1 + $var2 ;
    return $uitkomst;
}
function min_function($var1, $var2) {
    // Bereken min van var1 en var2
    $uitkomst = $var1 - $var2 ;
    return $uitkomst;
}

function product_function($var1, $var2) {
    // Bereken product van var1 en var2
    $uitkomst = $var1 * $var2 ;
    return $uitkomst;
}

function Modulo_function($var1, $var2) {
    // Bereken modulo van var1 en var2
    $uitkomst = $var1 % $var2 ;
    return $uitkomst;
}
?>